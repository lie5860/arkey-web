#!/usr/bin/env node
import { createHash } from "node:crypto";
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const profilePath = resolve(root, "profiles/keychron-q6-pro-ansi.json");
const effectsPath = resolve(root, "profiles/effects-v1.json");
const outputPath = resolve(root, "firmware/qmk/arkey_generated.h");
const profile = JSON.parse(readFileSync(profilePath, "utf8"));
const effects = JSON.parse(readFileSync(effectsPath, "utf8"));

const hashSource = JSON.stringify({
  controls: profile.controls,
  encoder: profile.encoder,
  ledCount: profile.ledCount,
  matrix: profile.matrix,
});
const layoutHash = createHash("sha256").update(hashSource).digest("hex");
if (profile.layoutHash !== layoutHash) throw new Error(`Profile layoutHash mismatch: expected ${layoutHash}`);

const expectedPrimitives = ["off", "solid", "shallowBreath", "breath", "doublePulse", "riseFade", "pressFlash"];
for (const [index, name] of expectedPrimitives.entries()) {
  if (effects.primitives?.[name] !== index) throw new Error(`Primitive ${name} must have firmware id ${index}`);
}
const atmospherePercent = Math.round(Number(effects.atmosphereMix) * 100);
if (atmospherePercent < 0 || atmospherePercent > 100 || Math.abs(atmospherePercent / 100 - effects.atmosphereMix) > 1e-9) {
  throw new Error("atmosphereMix must map exactly to an integer percent");
}
if (profile.matrix.rows * profile.matrix.columns > 128) throw new Error("Profile matrix no longer fits the 16-byte binding mask");
if (profile.ledCount > 255) throw new Error("Profile LED count no longer fits the wire contract");

const prefix = layoutHash.slice(0, 8).toUpperCase();
const enumLines = expectedPrimitives.map((name, index) =>
  `    AG_EFFECT_${name.replace(/([a-z])([A-Z])/g, "$1_$2").toUpperCase()} = ${index},`
).join("\n");
const generated = `/* SPDX-License-Identifier: MIT */
/* AUTO-GENERATED by scripts/generate-firmware-contract.mjs. DO NOT EDIT. */
#pragma once

#define AG_PROFILE_LAYOUT_HASH "${layoutHash}"
#define AG_PROFILE_CRC32 0x${prefix}UL
#define AG_PROFILE_LED_COUNT ${profile.ledCount}
#define AG_MATRIX_ROWS ${profile.matrix.rows}
#define AG_MATRIX_COLS ${profile.matrix.columns}
#define AG_DEFAULT_ATMOSPHERE_MIX ${atmospherePercent}
#define AG_SELECTED_PULSE_PERIOD_MS ${effects.selectedPulsePeriodMs}

enum arkey_effect {
${enumLines}
};
`;

if (process.argv.includes("--check")) {
  const current = readFileSync(outputPath, "utf8");
  if (current !== generated) throw new Error("firmware/qmk/arkey_generated.h is stale; run npm run generate:firmware-contract");
  process.stdout.write(`Firmware contract is current (${layoutHash.slice(0, 8)}).\n`);
} else {
  writeFileSync(outputPath, generated);
  process.stdout.write(`Generated ${outputPath}\n`);
}
